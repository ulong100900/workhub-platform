// Вспомогательная функция для поиска городов по региону
export const getCitiesByRegionId = (regionId: string): City[] => {
  return cities.filter(city => city.regionId === regionId)
}

// Вспомогательная функция для поиска региона по ID
export const getRegionById = (regionId: string): Region | undefined => {
  return regions.find(region => region.id === regionId)
}

// Вспомогательная функция для поиска города по ID
export const getCityById = (cityId: string): City | undefined => {
  return cities.find(city => city.id === cityId)
}

// Функция для получения отсортированных городов по региону
export const getSortedCitiesByRegionId = (regionId: string): City[] => {
  return getCitiesByRegionId(regionId).sort((a, b) => 
    a.name.localeCompare(b.name, 'ru')
  )
}

// Функция для получения всех городов отсортированных по алфавиту
export const getAllSortedCities = (): City[] => {
  return [...cities].sort((a, b) => a.name.localeCompare(b.name, 'ru'))
}

